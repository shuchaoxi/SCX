# Additional single-character replacements (only process after multi-char)
# These are handled by the main replacement loop

def process_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    original = content
    
    # Apply phrase replacements (longer patterns first - already sorted by length in our list)
    for old, new in PHRASES:
        content = content.replace(old, new)
    
    # Clean up whitespace artifacts
    content = re.sub(r' {2,}', ' ', content)
    content = re.sub(r' \.', '.', content)
    content = re.sub(r' ,', ',', content)
    content = re.sub(r' ;', ';', content)
    content = re.sub(r' :', ':', content)
    
    if content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
        return True
    return False

def main():
    count = 0
    for f in FILES:
        full = os.path.join(os.path.dirname(__file__) or '.', f)
        full = os.path.normpath(full)
        if not os.path.exists(full):
            full = os.path.join('F:', 'scx', f.replace('/', '\\'))
            full = os.path.normpath(full)
        if os.path.exists(full):
            changed = process_file(full)
            if changed:
                count += 1
                print(f"вң“ {f}")
            else:
                print(f"- {f} (no changes)")
        else:
            print(f"вң— {f} NOT FOUND at {full}")
    print(f"\nTotal files modified: {count}/{len(FILES)}")

if __name__ == '__main__':
    main()
